-- GosthRecon / GHOSTRECON: runs, findings, bounty_intel (corpus deduplicado)

create table if not exists public.runs (
  id bigint generated by default as identity primary key,
  target text not null,
  exact_match boolean not null default false,
  modules_json jsonb not null,
  stats_json jsonb not null,
  correlation_json jsonb,
  created_at timestamptz not null default now()
);

create table if not exists public.findings (
  id bigint generated by default as identity primary key,
  run_id bigint not null references public.runs (id) on delete cascade,
  type text,
  prio text,
  score int,
  value text,
  meta text,
  url text
);

create index if not exists findings_run_id_idx on public.findings (run_id);

create table if not exists public.bounty_intel (
  id bigint generated by default as identity primary key,
  target text not null,
  fp text not null,
  type text,
  prio text,
  score int,
  value text,
  meta text,
  url text,
  first_seen timestamptz not null,
  last_seen timestamptz not null,
  last_run_id bigint references public.runs (id),
  unique (target, fp)
);

create index if not exists bounty_intel_target_idx on public.bounty_intel (target);

alter table public.runs enable row level security;
alter table public.findings enable row level security;
alter table public.bounty_intel enable row level security;

drop policy if exists "ghostrecon_runs_all" on public.runs;
create policy "ghostrecon_runs_all" on public.runs for all using (true) with check (true);

drop policy if exists "ghostrecon_findings_all" on public.findings;
create policy "ghostrecon_findings_all" on public.findings for all using (true) with check (true);

drop policy if exists "ghostrecon_intel_all" on public.bounty_intel;
create policy "ghostrecon_intel_all" on public.bounty_intel for all using (true) with check (true);

grant usage on schema public to anon, authenticated;
grant all on table public.runs to anon, authenticated;
grant all on table public.findings to anon, authenticated;
grant all on table public.bounty_intel to anon, authenticated;
grant usage, select on all sequences in schema public to anon, authenticated;
